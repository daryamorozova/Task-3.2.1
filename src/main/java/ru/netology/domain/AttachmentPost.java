package ru.netology.domain;

public class AttachmentPost {
    private String imagePost;
    private String videoPost;
    private String audioPost;
    private String articlePost;
    private String documentPost;
    private String mapPost;
    private String graffityPost;
    private String pollPost;
    private String productPost;

    public String getImagePost() {
        return imagePost;
    }

    public void setImagePost(String imagePost) {
        this.imagePost = imagePost;
    }

    public String getVideoPost() {
        return videoPost;
    }

    public void setVideoPost(String videoPost) {
        this.videoPost = videoPost;
    }

    public String getAudioPost() {
        return audioPost;
    }

    public void setAudioPost(String audioPost) {
        this.audioPost = audioPost;
    }

    public String getArticlePost() {
        return articlePost;
    }

    public void setArticlePost(String articlePost) {
        this.articlePost = articlePost;
    }

    public String getDocumentPost() {
        return documentPost;
    }

    public void setDocumentPost(String documentPost) {
        this.documentPost = documentPost;
    }

    public String getMapPost() {
        return mapPost;
    }

    public void setMapPost(String mapPost) {
        this.mapPost = mapPost;
    }

    public String getGraffityPost() {
        return graffityPost;
    }

    public void setGraffityPost(String graffityPost) {
        this.graffityPost = graffityPost;
    }

    public String getPollPost() {
        return pollPost;
    }

    public void setPollPost(String pollPost) {
        this.pollPost = pollPost;
    }

    public String getProductPost() {
        return productPost;
    }

    public void setProductPost(String productPost) {
        this.productPost = productPost;
    }
}
